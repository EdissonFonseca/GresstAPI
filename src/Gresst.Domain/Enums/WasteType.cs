public enum WasteType
{
    Weee = 1,        // Waste Electrical and Electronic Equipment
    Hazardous,       // Hazardous to health or environment — industrial, hospital, pharma
    Hydrocarbon,     // Oils, solvents, fuels — specific valorization processes
    Recoverable,     // Recyclable — paper, glass, metal, plastic, textile, tires
    Organic,         // Biodegradable — agricultural, food, biogas feedstock
    Inert,           // Non-reactive — construction and demolition
    Mixed            // Unsegregated — multiple types pending classification
}